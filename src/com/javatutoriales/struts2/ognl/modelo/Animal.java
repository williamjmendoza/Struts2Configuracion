package com.javatutoriales.struts2.ognl.modelo;

public class Animal
{
	private String nombre;
	private String raza;
	
	public String getNombre() {
		return nombre;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public String getRaza() {
		return raza;
	}
	
	public void setRaza(String raza) {
		this.raza = raza;
	}	
	
}
